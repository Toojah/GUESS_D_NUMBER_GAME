import random
import sys

levels = input("Type Easy or Hard..   ")
counter = 0
if levels == "easy":
    counter = 8
elif levels == "hard":
    counter = 6

hidden_number = random.randint(1, 100)


def num_check():
    num_guess = input("""Guess the Hidden Number......""")
    num_guess = int(num_guess)
    if num_guess == hidden_number:
        print(f"Congratulations, You have successfully guessed the correct Number {hidden_number}")
        sys.exit()
    elif num_guess > hidden_number:
        print("Too High")
    elif num_guess < hidden_number:
        print("Too Low")


for i in range(1, counter):
    num_check()
    i = i + 1
    if i == counter:
        print("Game Over")
        break
    x = counter - i
    print(f"you have {x} attempts remaining")

